# Taking input from user as List

list = [] # Empty List
# Taking input from user

n = int(input("Enter range for elements : "))
print("Enter fruits name :")
for i in range(0, n):
    fruits = input("\n")
# Adding the values in a list
    list.append(fruits)

print(list) # Print the list

# Performing some list methods
# INSERT method simply insert the value at particular index i.e., input new fruit at index 1

print("Enter to insert")
list.insert(1 , input())
print(list)

#REMOVE method removes the entered value from the new list

print("Enter to remove")
list.remove(input())
print(list)

#Return length of a list

print(len(list))
